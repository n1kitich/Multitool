import UIKit

extension UIColor {
    static var textBrown: UIColor {
        return UIColor(red: 0.17, green: 0.077, blue: 0.077, alpha: 1)
    }
    
    static var lightYellow: UIColor {
        return UIColor(red: 0.96, green: 0.96, blue: 0.93, alpha: 1.00)
    }
    
    static var orange: UIColor {
        return UIColor(red: 0.98, green: 0.60, blue: 0.05, alpha: 1.00)
    }
    
    static var shadowGray: UIColor {
        return UIColor(red: 0.96, green: 0.96, blue: 0.96, alpha: 1)
    }
}
